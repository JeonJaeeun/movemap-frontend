function convertRawDataToDropdownFormat(rawData) {
    const addressList = rawData.flatMap((item) => item.data || []);

    const addressTree = {};

    const validAddresses = addressList.filter(
        (item) => !item.삭제일자 && item.시도명 && item.시군구명
    );

    validAddresses.forEach((item) => {
        const city = item.시도명;
        const district = item.시군구명;

        if (!addressTree[city]) {
            addressTree[city] = { districts: new Set() };
        }
        addressTree[city].districts.add(district);
    });

    const finalAddressData = Object.keys(addressTree).map((cityName) => {
        const districtList = Array.from(addressTree[cityName].districts).map(
            (districtName) => ({
                label: districtName,
                value: districtName,
                towns: [],
            })
        );

        return {
            label: cityName,
            value: cityName,
            districts: districtList.sort((a, b) =>
                a.label.localeCompare(b.label)
            ),
        };
    });

    // 시/도 가나다순 정렬
    return finalAddressData.sort((a, b) => a.label.localeCompare(b.label));
}

